GLuint fontOffset;

GLuint getElement(int row, int col) {
    return NewFont[col * FONTW + row];
}

GLubyte newLetter [24*3];
void getLetter(int row, int col) {
    int p = 0;
    for (int i = row*24-10; i < (24 + row*24)-1; i++) {
        for (int j = col*3; j < (3 + 3*col) ;++j) {
            newLetter[p] = getElement(j,i);
            p++;
        }
    }
}

void makeRasterFont() {
    GLuint i, j;
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    fontOffset = glGenLists (128);
    // letters
    int startx= 10;
    int starty = 0;
    for (i = 0,j = 32; i < 100; i++,j++) {
        getLetter(startx,starty);
        starty++;
        if (starty == 10) {
            starty = 0;
            startx-=1;
        }
        glNewList(fontOffset + j, GL_COMPILE);
        if (j == 'M') {
            glBitmap(24, 24, 0.0, 2.0, 22.0, 0.0, newLetter);
        } else {
            glBitmap(24, 24, 0.0, 2.0, 19.0, 0.0, newLetter);
        }
        glEndList();
    }
    glEndList();
}


void printString(std::string s) {
	char str[s.length()+1];
	strcpy(str, s.c_str());
	glPushAttrib (GL_LIST_BIT);
	glListBase(fontOffset);
	glCallLists(s.length(), GL_UNSIGNED_BYTE, (GLubyte *) (str));
	glPopAttrib ();
}